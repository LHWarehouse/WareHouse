package com.warehouse.pojo;


public class Goods
{
	private String goods_id;
	private String goods_name;
	private String goods_color;
	private Long goods_price;
	private Long goods_total;
	private Long goods_status;
	private String goods_category_id;
	
	
	public String getGoods_id()
	{
		return goods_id;
	}
	public void setGoods_id(String goods_id)
	{
		this.goods_id = goods_id;
	}
	public String getGoods_name()
	{
		return goods_name;
	}
	public void setGoods_name(String goods_name)
	{
		this.goods_name = goods_name;
	}
	public String getGoods_color()
	{
		return goods_color;
	}
	public void setGoods_color(String goods_color)
	{
		this.goods_color = goods_color;
	}
	public Long getGoods_price()
	{
		return goods_price;
	}
	public void setGoods_price(Long goods_price)
	{
		this.goods_price = goods_price;
	}
	public Long getGoods_total()
	{
		return goods_total;
	}
	public void setGoods_total(Long goods_total)
	{
		this.goods_total = goods_total;
	}
	public Long getGoods_status()
	{
		return goods_status;
	}
	public void setGoods_status(Long goods_status)
	{
		this.goods_status = goods_status;
	}
	public String getGoods_category_id()
	{
		return goods_category_id;
	}
	public void setGoods_category_id(String goods_category_id)
	{
		this.goods_category_id = goods_category_id;
	}
	
}
