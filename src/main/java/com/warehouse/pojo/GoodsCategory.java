package com.warehouse.pojo;

public class GoodsCategory
{
	
	private String category_id;
	private String category_type_code;
	private String category_type_name;
	private String category_goods_name;
	
	public String getCategory_id()
	{
		return category_id;
	}
	public void setCategory_id(String category_id)
	{
		this.category_id = category_id;
	}
	public String getCategory_type_code()
	{
		return category_type_code;
	}
	public void setCategory_type_code(String category_type_code)
	{
		this.category_type_code = category_type_code;
	}
	public String getCategory_type_name()
	{
		return category_type_name;
	}
	public void setCategory_type_name(String category_type_name)
	{
		this.category_type_name = category_type_name;
	}
	public String getCategory_goods_name()
	{
		return category_goods_name;
	}
	public void setCategory_goods_name(String category_goods_name)
	{
		this.category_goods_name = category_goods_name;
	}
	
}
